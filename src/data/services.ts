// ✅ Helper functions
export const getProvidersByCity = (city: string) => {
  return providers.filter(provider => provider.city === city);
};

export const getProvidersByCategoryAndCity = (category: string, city: string) => {
  return providers.filter(provider => provider.category === category && provider.city === city);
};

export const getProviderById = (id: string) => {
  return providers.find(provider => provider.id === id);
};

export const getAvailableCities = () => {
  const cities = [...new Set(providers.map(p => p.city))];
  return cities;
};

export const getCategoriesByCity = (city: string) => {
  const availableCategories = providers
    .filter(p => p.city === city)
    .map(p => p.category);
  return [...new Set(availableCategories)];
};